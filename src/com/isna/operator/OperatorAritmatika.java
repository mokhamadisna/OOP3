package com.isna.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 9 + 10;
        System.out.println("Hasil 9 + 10 = " + hasilPenjumlahan);
        System.out.println();
        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 9 - 18;
        System.out.println("Hasil 9 - 18 = " + hasilPengurangan);
        System.out.println();
        System.out.println("Operasi Pengalian");
        int hasilPengalian = 56 * 55;
        System.out.println("Hasil 56 * 55 = " + hasilPengalian);
        System.out.println();
        System.out.println("Operasi Pembagian");
        int hasilPembagian = 12 / 2;
        System.out.println("Hasil 12 / 2 = " + hasilPembagian);
        System.out.println();
        System.out.println("Operasi Habis bagi");
        int hasilSisa = 18 % 3;
        System.out.println("Hasil 18 % 3 = " + hasilSisa);
        int hasilSisaLain = 9 % 4;
        System.out.println("Hasil 9 % 4 = " + hasilSisaLain);
        System.out.println();
    }
}
